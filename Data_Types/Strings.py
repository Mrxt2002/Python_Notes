#take a string
course='python for beginners'
#prints in upper cases
print(course.upper())
#prints in lower cases
print(course.lower())

#finds charaacter in string
print(course.find('for'))

#replace character in string
#syntax is: var_name.replace('target word','replace word')
print(course.replace('for','699'))

#get a boolean output upon search
print('python' in course)