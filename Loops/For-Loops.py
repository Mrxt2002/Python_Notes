# Declare a variable named 'numbers' and assign it a list of numbers
numbers = [1, 2, 3, 4, 5]

# Using a 'for' loop to iterate through the 'numbers' list
for item in numbers:
    print(item)
# Output: 1
#         2
#         3
#         4
#         5

# Using a 'while' loop to iterate through the 'numbers' list
i = 0
while i < len(numbers):
    print(numbers[i])
    i = i + 1
# Output: 1
#         2
#         3
#         4
#         5
